package model;

/**
 * This is the interface for pixel.
 */
public interface IPixel {

  /**
   * Gets the value of Red configuration of this pixel.
   *
   * @return red value of this pixel.
   */
  int getRed();

  /**
   * Gets the value of Green configuration of this pixel.
   *
   * @return green value of the pixel
   */
  int getGreen();

  /**
   * Gets the value of Blue configuration of this pixel.
   *
   * @return blue value of the pixel
   */
  int getBlue();
}
